<template>
  <div class="flex flex-row justify-between" style="width: 100%">
    <!-- <el-statistic title="白屏时间" :value="ntFormat('FP')">
      <template #suffix>ms</template>
    </el-statistic>
    <el-statistic title="DOM Ready" :value="ntFormat('FCP')">
      <template #suffix>ms</template>
    </el-statistic>
    <el-statistic title="首次可交互时间" :value="ntFormat('LCP')">
      <template #suffix>ms</template>
    </el-statistic> -->
  </div>
  <Echarts :options="navigationEchart"></Echarts>
</template>

<script lang="ts">
import Echarts from "@/components/echarts/index.vue";
import { computed } from "vue";
export default {
  components: {
    Echarts,
  },
  props: {
    options: {
      type: Object,
      required: true,
    },
  },
  name: "wxPerfEchart",
  setup(props) {
    /**
     * @description: 性能指标图
     * @param {*} computed
     * @return {*}
     */
    const navigationEchart = computed(() => {
      const NT: any = props.options?.nt || {};
      // if (!NT || !Object.keys(NT).length) return {};
      const axisData = [
        "firstRenderduration",
        "evaluateScriptduration",
        "routeduration",
        "appLaunchduration",
        "downloadPackageduration"
      ];

      const seriesData: any[] = [];
      const yAxisLeftData: any[] = [
        "页面渲染",
        "JS注入",
        "路由",
        "小程序启动",
        "代码包下载",
      ];
      const yAxisRightData: any[] = [];
      axisData.forEach((item, index) => {
        const value = (NT[item] || 0).toFixed(2);
        yAxisRightData.push(`${value}ms`);
        const data: any = {
          name: item,
          type: "bar",
          stack: "Total",
          label: {
            show: true,
            position: "right",
          },
          data: [],
        };
        // 重置 性能节点数据
        for (let i = 0, len = axisData.length; i < len; i++) {
          if (index == i) {
            data.data.push(value);
          } else {
            data.data.push("-");
          }
        }
        seriesData.push(data);
      });
      const {
        firstRenderduration = 0,
        evaluateScriptduration = 0,
        routeduration = 0,
        appLaunchduration = 0,
        downloadPackageduration = 0
      }: any = NT;

      const ntSeriesData: number[] = [
        +firstRenderduration.toFixed(2),
        +evaluateScriptduration.toFixed(2),
        +routeduration.toFixed(2),
        +appLaunchduration.toFixed(2),
        +downloadPackageduration.toFixed(2),
      ];
      const defaultSeriesData: any[] = [];
      for (let i = 0; i < ntSeriesData.length; i++) {
        const sum = JSON.parse(JSON.stringify(ntSeriesData))
          .reverse()
          .slice(0, i + 1)
          .reduce((a: any, b: any) => a + b);
        defaultSeriesData.unshift(+sum.toFixed(2));
      }

      const { placeholderData } = getSeriesData(defaultSeriesData.reverse());

      const option = {
        tooltip: {
          trigger: "axis",
          axisPointer: {
            type: "shadow",
          },
          formatter: function (params: any) {
            for (let i = 1, len = params.length; i < len; i++) {
              let { name, value, seriesName, color } = params[i];
              if (value !== "-") {
                return `${name}</br><span style="display:flex; align-items: center;">
					<i style="display:block;margin-right:5px;border-radius:50%;width:10px;height:10px;left:5px;background-color:${color}"></i>${seriesName}:
					<i style="padding-left: 30px;">${value}</i>
				</span>`;
              }
            }
          },
        },
        xAxis: {
          type: "value",
          show: false,
          axisLine: {
            show: false,
          },
          axisTick: {
            show: false,
          },
          splitLine: {
            lineStyle: {
              type: "dotted",
            },
          },
        },
        yAxis: [
          {
            axisLine: {
              show: false,
            },
            axisTick: {
              show: false,
            },
            splitLine: {
              lineStyle: {
                type: "dotted",
              },
            },
            type: "category",
            data: yAxisLeftData,
          },
          {
            axisLine: {
              show: false,
            },
            axisTick: {
              show: false,
            },
            splitLine: {
              lineStyle: {
                type: "dotted",
              },
            },
            type: "category",
            data: yAxisRightData,
          },
        ],
        seriesColorReset: false,
        series: [
          {
            name: "Placeholder",
            type: "bar",
            stack: "Total",
            silent: true,
            itemStyle: {
              borderColor: "transparent",
              color: "transparent",
            },
            emphasis: {
              itemStyle: {
                borderColor: "transparent",
                color: "transparent",
              },
            },
            data: placeholderData.reverse(),
          },
          ...seriesData,
        ],
      };
      // console.log("option---", option);
      return option;
    });

    function getSeriesData(oData: any[]) {
      const placeholderData: number[] = [0];
      const data1: number[] = [oData[0]];
      const data2: number | string[] = ["-"];

      oData.forEach((item, inx) => {
        if (inx !== oData.length - 1) {
          const diffVal: any = oData[inx + 1] - item;
          if (diffVal > 0) {
            placeholderData.push(item);
            data1.push(diffVal);
            data2.push("-");
          } else {
            placeholderData.push(item + diffVal);
            data1.push(0);
            data2.push(Math.abs(diffVal));
          }
        }
      });
      return { placeholderData, data1, data2 };
    }

    return {
      navigationEchart,
    };
  },
};
</script>
